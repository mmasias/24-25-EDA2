package ejercicios.Reyes_david.Ejercicio_1;

public class Coordenada {

    private int x;
    private int y;

    public Coordenada(int columna, int fila) {
        x = columna;
        y = fila;    
    }

    public int obtenerX() {
        return x;
    }

    public int obtenerY() {
        return y;
    }

}