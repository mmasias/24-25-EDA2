public class Resolucion {
    private final int ancho;
    private final int alto;
    
    public Resolucion(int ancho, int alto) {
        this.ancho = ancho;
        this.alto = alto;
    }

    public int getAncho(){
        return this.ancho;
    }

    public int getAlto(){
        return this.alto;
    }

}