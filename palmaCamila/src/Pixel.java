public class Pixel {
    private char asterisco;
    private char punto;
    private char mas;

    public Pixel() {
        this.asterisco = '*';
        this.punto = '.';
        this.mas = '+';
    }

    public char getAsterisco() {
        return asterisco;
    }

    public char getPunto() {
        return punto;
    }

    public char getMas() {
        return mas;
    }
}
